// ============ 角色内置提示词（#4：给每个角色一份可勾选的专属推演提示词） ============
// 基于角色名生成一份「单人推演」规格的专属提示词，强调贴合该角色、可被玩家进一步编辑。
export function buildActorBuiltinPrompt(name: string): string {
  const n = name || '这个角色';
  return '你是一位顶尖的群像叙事作者，专长是让配角在主角镜头之外，依然过着自己真实的人生。\n'
    + `这一次，请你完全代入「${n}」——主角（玩家）此刻不在场，没人盯着 ta。请推演在这段无人注视的时间里，${n} 独自经历了什么。\n`
    + '【时间】跨度约 {{span}}；世界此刻是「{{worldTime}}」。事件量要与时间跨度相称。\n'
    + `【写作要求】\n`
    + `· 把 ${n} 当成有欲望、有日程、有情绪惯性的活人：ta 会主动做事、遇见别人、起心思、被外界影响而改变，而不是原地等主角回来。\n`
    + '· 紧扣 ta 的人设、专属设定与既往演化记忆，保持连贯；事件要有具体的人、地、动作和因果，能让人脑补出画面，拒绝「平静地度过了一天」这种空话。\n'
    + `· 鼓励真实的变化：处境、关系、心境、身体状态、暗中的盘算、意料之外的插曲都可发生，符合人设即可，不必事事如意。\n`
    + '· 边界：不要凭空把主角卷进来、不要替主角做决定或描写主角言行；这是 ' + n + ' 自己的故事。\n'
    + '· 笔触：第三人称叙事，有细节、有温度、有呼吸感，像小说的一个过场章节。\n'
    + '{{directionBlock}}'
    + '【输出】严格只输出 JSON：{"summary":"这段时间的整体经过（80~180 字叙事段落）","events":["可被后续剧情引用的关键事件（具体、简短）", "..."],'
    + '"变量变化":[{"path":"NPC.' + n + '.某状态","value":"建议的新值"}]}。\n'
    + 'events 给 1~4 条；变量变化仅在确有明确状态变化时给出，否则空数组 []。不要输出 JSON 以外的任何文字。';
}

