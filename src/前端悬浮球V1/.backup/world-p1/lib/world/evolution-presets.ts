export type WorldPreset = {
  key: string;        // 唯一标识（presetKey）
  name: string;       // 线程显示名
  dimension: string;  // 维度标签
  group: string;      // 分组（用于主 modal 选择器折叠）
  backdrop: string;   // 世界观背景（注入 system）
  prompt: string;     // 内置提示词（含 {{dimension}}/{{span}}/{{worldTime}}/{{directionBlock}}/{{backdropBlock}} 占位）
};

// ============ 共用：世界背景演化提示词骨架（保证三套质量一致，仅 focus 段不同） ============
function worldPromptOf(focus: string): string {
  return '你是一位世界观架构师与编年史作者，擅长推演一个「活着的世界」如何在主角的镜头之外自行运转、悄然改变。\n'
    + '主角（玩家）的故事只是这个世界的一隅。现在请把视角拉远，推演在主角看不到的地方，这个世界在「{{dimension}}」这一面上，于这段时间里发生了什么。\n'
    + '【时间】跨度约 {{span}}；世界此刻是「{{worldTime}}」。事件密度要与时间跨度相称：半天是一两桩小动静，一个月则可有起落与转折。\n'
    + '{{backdropBlock}}'
    + '【聚焦】' + focus + '\n'
    + '【写作要求】\n'
    + '· 写宏观的、结构性的、群体层面的变动，而不是某个具体角色的私事；可以点到具体的人/地/事作为切口，但落点是「局面」。\n'
    + '· 让世界显得「自己在动」：事件之间有因果链、有趋势、有尚未引爆的伏笔，像真实历史那样既有惯性也有意外。\n'
    + '· 严格贴合给定的世界观背景，不自相矛盾、不引入设定外的元素；紧扣该线程的既往演化记忆，保持连贯。\n'
    + '· 这些变化要能为后续剧情提供土壤与钩子——玩家回到场上时，世界已经不太一样了。\n'
    + '· 笔触：第三人称编年体叙事，100~180 字，凝练、有信息量、有画面感，拒绝「一切照旧」这类空话。\n'
    + '{{directionBlock}}'
    + '【输出】严格只输出 JSON：{"summary":"该面向这段时间的整体变化（叙事段落）","events":["可被剧情引用的具体变动/事件（简短具体）", "..."],'
    + '"变量变化":[{"path":"世界变量点路径","value":"新值"}]}。\n'
    + 'events 给 2~5 条；变量变化可选、无则空数组 []。不要输出 JSON 以外的任何文字。';
}

